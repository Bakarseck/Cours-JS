let leMax = (a,b) => {
    var le_max;
    if(a>b){
        le_max = a;
        return le_max
    } else {
        le_max = b;
        return le_max
    }
}

let leMin = (a,b) => {
    var le_min;
    if(b>a){
        le_min = a;
        return le_min
    } else {
        le_min = b;
        return le_min
    }
}
